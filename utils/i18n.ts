function interpolate(value: string, params?: Record<string, string | number>) {
  if (!params) {
    return value;
  }

  return Object.entries(params).reduce(
    (text, [key, replacement]) => text.replaceAll(`{{${key}}}`, String(replacement)),
    value
  );
}

export function translate(
  languageCode: AppLanguageCode,
  key: TranslationKey,
  params?: Record<string, string | number>
) {
  return interpolate(translations[languageCode]?.[key] ?? en[key] ?? key, params);
}

export function translateKnownText(languageCode: AppLanguageCode, value: string) {
  const matchingKey = (Object.keys(en) as TranslationKey[]).find((key) => en[key] === value);

  if (!matchingKey) {
    return value;
  }

  return translate(languageCode, matchingKey);
}

export function useI18n() {
  const languageCode = usePreferencesStore((state) => state.languageCode);

  return {
    languageCode,
    locale: getLocale(languageCode ?? defaultLanguageCode),
    t: (key: TranslationKey, params?: Record<string, string | number>) =>
      translate(languageCode ?? defaultLanguageCode, key, params),
  };
}

export function getLocale(languageCode: AppLanguageCode) {
  const locales: Record<AppLanguageCode, string> = {
    en: 'en-US',
    tr: 'tr-TR',
  };

  return locales[languageCode];
}
